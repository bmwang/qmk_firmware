unsigned int last_flicker_time = 0;
bool flicker = false;

void record_flicker_action(void) {
  last_flicker_time = timer_read();
  flicker = !flicker;
}

const char *read_flicker(void) {
    unsigned int cur_time = timer_read();
    if (flicker && (cur_time - last_flicker_time) < TIMEOUT)
    {
        return filled;
    }
    flicker = false;
    return empty;
}
